package com.rohit.model;

public class FarmerInfo {
	static int f_id;
	static String f_name, f_location, f_contact, f_email;
	
	public static int getF_id() {
		return f_id;
	}
	public static void setF_id(int f_id) {
		FarmerInfo.f_id = f_id;
	}
	public static String getF_name() {
		return f_name;
	}
	public static void setF_name(String f_name) {
		FarmerInfo.f_name = f_name;
	}
	public static String getF_location() {
		return f_location;
	}
	public static void setF_location(String f_location) {
		FarmerInfo.f_location = f_location;
	}
	public static String getF_contact() {
		return f_contact;
	}
	public static void setF_contact(String f_contact) {
		FarmerInfo.f_contact = f_contact;
	}
	public static String getF_email() {
		return f_email;
	}
	public static void setF_email(String f_email) {
		FarmerInfo.f_email = f_email;
	}
	
	
	
}
